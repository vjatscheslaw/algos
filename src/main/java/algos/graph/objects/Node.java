package algos.graph;

/**
 *
 */
public class Node {
    private final int id;

    public Node(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Node node = (Node) o;

        return this.id == node.id;
    }

    @Override
    public int hashCode() {
        return this.id;
    }
}
